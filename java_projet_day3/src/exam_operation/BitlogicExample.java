package exam_operation;

public class BitlogicExample {
	public static void main(String[] args) {
		System.out.println("5 & 10 = " + (5 & 10)); // & 두비트가 모두1이면 그렇지않으면 0
		System.out.println("5 | 10 = " + (5 | 10));
		System.out.println("5 ^ 10 = " + (5 ^ 10));
		System.out.println("~10 = " + (~10));
		
		
		
		
	}
}
