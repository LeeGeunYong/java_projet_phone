package exam_var;

public class VarExample {
	public static void main(String[] args){
		var price = 10000;
		var name = "홍길동";
		var pi = 3.1315292;
		
		System.out.println("가격(price) :  " + price + "원입니다");
		System.out.println("이름(name) : " + name  + "입니다"); 	
		System.out.println("pi : " + pi);	
	}


}

